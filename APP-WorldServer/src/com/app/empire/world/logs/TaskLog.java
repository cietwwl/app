package com.app.empire.world.logs;

public class TaskLog {
	private int taskId;
	private int getTaskNum;
	private int finishTaskNum;

	public int getTaskId() {
		return taskId;
	}

	public void setTaskId(int taskId) {
		this.taskId = taskId;
	}

	public int getGetTaskNum() {
		return getTaskNum;
	}

	public void setGetTaskNum(int getTaskNum) {
		this.getTaskNum = getTaskNum;
	}

	public int getFinishTaskNum() {
		return finishTaskNum;
	}

	public void setFinishTaskNum(int finishTaskNum) {
		this.finishTaskNum = finishTaskNum;
	}
}
